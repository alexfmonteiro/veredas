/** Widen literal string types to `string` and readonly arrays to `readonly string[]` */
type DeepStringify<T> = T extends readonly string[]
  ? readonly string[]
  : T extends string
    ? string
    : T extends object
      ? { readonly [K in keyof T]: DeepStringify<T[K]> }
      : T;

